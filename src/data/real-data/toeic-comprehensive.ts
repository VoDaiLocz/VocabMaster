// Helper function to create vocabulary words
const w = (term: string, definition: string, arg3: string, arg4?: string): VocabWord => {
    if (arg4 === undefined) {
        // Handle 3 arguments
        if (arg3.startsWith('/')) {
            return { term, definition, example: '', phonetic: arg3 }
        }
        return { term, definition, example: arg3, phonetic: '' }
    }
    return {
        term,
        definition,
        example: arg3,
        phonetic: arg4
    }
}


